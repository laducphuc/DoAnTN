package model.bean;

public class PhuongAn {

	private String maPhuongAn;
	private String maCauHoi;
	private String noiDungPA;
	private String hinhAnh;
	private String trangThai;
	private String soThuTuPA;
	private String coChon;
	
	
	public String getSoThuTuPA() {
		return soThuTuPA;
	}
	public void setSoThuTuPA(String soThuTuPA) {
		this.soThuTuPA = soThuTuPA;
	}
	public String getCoChon() {
		return coChon;
	}
	public void setCoChon(String coChon) {
		this.coChon = coChon;
	}
	public String getMaPhuongAn() {
		return maPhuongAn;
	}
	public void setMaPhuongAn(String maPhuongAn) {
		this.maPhuongAn = maPhuongAn;
	}
	public String getMaCauHoi() {
		return maCauHoi;
	}
	public void setMaCauHoi(String maCauHoi) {
		this.maCauHoi = maCauHoi;
	}
	public String getNoiDungPA() {
		return noiDungPA;
	}
	public void setNoiDungPA(String noiDungPA) {
		this.noiDungPA = noiDungPA;
	}
	public String getHinhAnh() {
		return hinhAnh;
	}
	public void setHinhAnh(String hinhAnh) {
		this.hinhAnh = hinhAnh;
	}
	public String getTrangThai() {
		return trangThai;
	}
	public void setTrangThai(String trangThai) {
		this.trangThai = trangThai;
	}
	
}
