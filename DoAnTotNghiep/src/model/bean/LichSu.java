package model.bean;

public class LichSu {

	private String thoiGian;
	private String diem;
	private int stt;
	private String maLichSuThi;
	private String tenTaiKhoan;
	private String maCapDo;

	public String getMaLichSuThi() {
		return maLichSuThi;
	}

	public void setMaLichSuThi(String maLichSuThi) {
		this.maLichSuThi = maLichSuThi;
	}

	public String getTenTaiKhoan() {
		return tenTaiKhoan;
	}

	public void setTenTaiKhoan(String tenTaiKhoan) {
		this.tenTaiKhoan = tenTaiKhoan;
	}

	public int getStt() {
		return stt;
	}

	public void setStt(int stt) {
		this.stt = stt;
	}

	public String getThoiGian() {
		return thoiGian;
	}

	public void setThoiGian(String thoiGian) {
		this.thoiGian = thoiGian;
	}

	public String getDiem() {
		return diem;
	}

	public void setDiem(String diem) {
		this.diem = diem;
	}

	public String getMaCapDo() {
		return maCapDo;
	}

	public void setMaCapDo(String maCapDo) {
		this.maCapDo = maCapDo;
	}

}
