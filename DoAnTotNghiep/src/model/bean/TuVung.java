package model.bean;

public class TuVung {

	private String maTuVung;
	private String mucTu;
	private String amThanh;
	private String dichNghia;
	private String stt;
	
	public String getStt() {
		return stt;
	}

	public void setStt(String stt) {
		this.stt = stt;
	}

	public String getMaTuVung() {
		return maTuVung;
	}

	public void setMaTuVung(String maTuVung) {
		this.maTuVung = maTuVung;
	}

	public String getMucTu() {
		return mucTu;
	}

	public void setMucTu(String mucTu) {
		this.mucTu = mucTu;
	}

	public String getAmThanh() {
		return amThanh;
	}

	public void setAmThanh(String amThanh) {
		this.amThanh = amThanh;
	}

	public String getDichNghia() {
		return dichNghia;
	}

	public void setDichNghia(String dichNghia) {
		this.dichNghia = dichNghia;
	}

}
